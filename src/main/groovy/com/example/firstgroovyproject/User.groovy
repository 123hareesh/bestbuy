package com.example.firstgroovyproject



class User
{

    public String fname;
    public String Lname;
    public String name;
    public String email;

    String getFname() {
        return fname
    }

    void setFname(String fname) {
        this.fname = fname
    }

    String getLname() {
        return Lname
    }

    void setLname(String lname) {
        Lname = lname
    }

    String getName() {
        return name
    }

    void setName(String name) {
        this.name = name
    }

    String getEmail() {
        return email
    }

    void setEmail(String email)
    {
        this.email = email
    }


}
